#include "../ft_printf.h"

int	ft_putnbr(int n)
{	
	int	t;

	n = 0;
	t = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
		t++;
	}
	if (n > 9)
		t += ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
	t++;
	return (t);
}
